print("Suma, Resta, Division y Multiplicacion de 2 numeros")
print("")
num01=input("Introduce el valor del primer numero: ")
num01=float(num01)

num02=input("Introduce el valor del segundo numero: ")
num02=float(num02)

suma=num01+num02
resta=num01-num02
division=num01/num02
multiplicacion=num01*num02
print("")
print("Ejemplo de suma: {} + {} = {}".format(num01,num02,suma))
print("Ejemplo de resta: {} - {} = {}".format(num01,num02,resta))
print("Ejemplo de division: {} / {} = {}".format(num01,num02,division))
print("Ejemplo de multiplicacion: {} * {} = {}".format(num01,num02,multiplicacion))
